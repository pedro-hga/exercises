function merge(nums1, m, nums2, n) {
    var i = m - 1;
    var j = n - 1;
    var k;
    m >= n ? k = i : k = j;
    if (nums1.length >= m)
        nums1.splice(m);
    for (k; k >= 0; k--) {
        if (n == 0)
            return nums1;
        nums1.splice(k, 0, nums2[j]);
        j--;
        if (j < 0)
            break;
    }
    return nums1.sort(function (a, b) { return a - b; });
}
